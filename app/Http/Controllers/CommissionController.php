     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CommissionRequest $request)
    {
        $setting = new Setting;
        $setting->create([
            'key'=>'commission',
            'value'=>$request->commission
        ]);
        return redirect()->back();
    }

